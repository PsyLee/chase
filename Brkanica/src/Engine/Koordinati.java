package Engine;

public class Koordinati {

    public int x;
    public int z;

    public Koordinati() {

    }

    public Koordinati(int x, int y) {
        this.x = x;
        this.z = y;
    }

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }

}
